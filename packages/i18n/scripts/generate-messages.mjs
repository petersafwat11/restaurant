#!/usr/bin/env node
/**
 * Walks `packages/i18n/messages/{en,pl}/**\/*.json` and emits
 * `src/messages.ts` with static imports + an EN/PL tree object.
 *
 * Naming convention:
 *   - File `messages/en/web/auth/login.json` becomes
 *     identifier `enWebAuthLogin` and is placed at `web.auth.login` in the
 *     EN_MESSAGES tree.
 *   - Folders/files with `kebab-case` segments become `camelCase` keys
 *     (e.g. `forgot-password.json` → `forgotPassword`).
 *
 * The EN and PL trees MUST mirror each other (same set of relative paths).
 * Mismatches abort the generator.
 */
import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const MESSAGES_DIR = path.join(__dirname, '..', 'messages');
const OUTPUT = path.join(__dirname, '..', 'src', 'messages.ts');

async function walk(dir, base = '') {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const full = path.join(dir, entry.name);
    const rel = base ? `${base}/${entry.name}` : entry.name;
    if (entry.isDirectory()) {
      files.push(...(await walk(full, rel)));
    } else if (entry.name.endsWith('.json')) {
      files.push(rel.replace(/\\/g, '/'));
    }
  }
  return files;
}

function kebabToCamel(s) {
  return s.replace(/-([a-z0-9])/g, (_, c) => c.toUpperCase());
}

function pascal(s) {
  const camel = kebabToCamel(s);
  return camel[0].toUpperCase() + camel.slice(1);
}

/** 'en' + 'web/auth/login.json' → 'enWebAuthLogin' */
function toIdentifier(locale, relPath) {
  const segments = relPath.replace(/\.json$/, '').split('/');
  return locale + segments.map(pascal).join('');
}

/** 'web/auth/login.json' → ['web', 'auth', 'login'] (kebab→camel each segment) */
function toNamespace(relPath) {
  return relPath
    .replace(/\.json$/, '')
    .split('/')
    .map(kebabToCamel);
}

function buildTree(files, locale) {
  const root = {};
  for (const f of files) {
    const ns = toNamespace(f);
    let node = root;
    for (let i = 0; i < ns.length - 1; i++) {
      if (typeof node[ns[i]] === 'string') {
        throw new Error(`Tree conflict at ${ns.slice(0, i + 1).join('.')} — leaf vs branch`);
      }
      node[ns[i]] = node[ns[i]] || {};
      node = node[ns[i]];
    }
    const leaf = ns[ns.length - 1];
    if (node[leaf] && typeof node[leaf] !== 'string') {
      throw new Error(`Tree conflict at ${ns.join('.')} — branch vs leaf`);
    }
    node[leaf] = `__VAR__${toIdentifier(locale, f)}__`;
  }
  return root;
}

function stringifyTree(obj, indent = 2) {
  const pad = ' '.repeat(indent);
  const closePad = ' '.repeat(Math.max(indent - 2, 0));
  const entries = Object.entries(obj);
  if (entries.length === 0) return '{}';
  const lines = entries.map(([k, v]) => {
    const valueLiteral =
      typeof v === 'string' && v.startsWith('__VAR__')
        ? v.replace(/^__VAR__/, '').replace(/__$/, '')
        : stringifyTree(v, indent + 2);
    return `${pad}${k}: ${valueLiteral},`;
  });
  return `{\n${lines.join('\n')}\n${closePad}}`;
}

async function generate() {
  const enFiles = (await walk(path.join(MESSAGES_DIR, 'en'))).sort();
  const plFiles = (await walk(path.join(MESSAGES_DIR, 'pl'))).sort();

  if (enFiles.length !== plFiles.length || enFiles.some((f, i) => f !== plFiles[i])) {
    const missingPL = enFiles.filter((f) => !plFiles.includes(f));
    const missingEN = plFiles.filter((f) => !enFiles.includes(f));
    const lines = [];
    if (missingPL.length) lines.push(`Missing in pl/: ${missingPL.join(', ')}`);
    if (missingEN.length) lines.push(`Missing in en/: ${missingEN.join(', ')}`);
    throw new Error(`en/ and pl/ message trees must mirror each other.\n${lines.join('\n')}`);
  }

  const enImports = enFiles
    .map((f) => `import ${toIdentifier('en', f)} from '../messages/en/${f}';`)
    .join('\n');
  const plImports = enFiles
    .map((f) => `import ${toIdentifier('pl', f)} from '../messages/pl/${f}';`)
    .join('\n');

  const enTreeLiteral = stringifyTree(buildTree(enFiles, 'en'));
  const plTreeLiteral = stringifyTree(buildTree(enFiles, 'pl'));

  const content = `// AUTO-GENERATED by scripts/generate-messages.mjs — do not edit by hand.
// Run \`pnpm --filter @repo/i18n generate:messages\` to regenerate after
// adding or removing message JSON files.
import type { Locale } from './locale';

${enImports}

${plImports}

const EN_MESSAGES = ${enTreeLiteral} as const;

const PL_MESSAGES = ${plTreeLiteral} as const;

export type AppMessages = typeof EN_MESSAGES;

/**
 * Returns the merged message tree for \`locale\`, used by next-intl on web/admin.
 * The shape is type-locked to the English tree; \`i18n.test.ts\` verifies that
 * EN and PL key sets are identical at runtime.
 */
export function loadMessages(locale: Locale): AppMessages {
  return (locale === 'pl' ? PL_MESSAGES : EN_MESSAGES) as AppMessages;
}
`;

  await fs.writeFile(OUTPUT, content);
  console.log(`Generated ${OUTPUT} from ${enFiles.length} message files per locale.`);
}

generate().catch((e) => {
  console.error(e);
  process.exit(1);
});
